import { create } from 'zustand'
import { immer } from 'zustand/middleware/immer'
import { supabase } from '../lib/supabase'
import { Order, OrderWithItems, CartItem, Payment } from '../types'
import { generatePromptPayPayload, generateQRReference } from '../lib/qr'
import { Config } from '../../constants/config'

interface OrderState {
  orders: OrderWithItems[]
  currentOrder: OrderWithItems | null
  isLoading: boolean

  createOrder: (
    shopId: string,
    cashierId: string,
    items: CartItem[],
    paymentMethod: 'cash' | 'qr' | 'card',
    discount: number,
    taxRate: number
  ) => Promise<Order>
  updateOrderStatus: (orderId: string, status: string) => Promise<void>
  completeOrder: (orderId: string, paymentData: Partial<Payment>, confirmationType: 'manual' | 'auto', confirmedBy?: string) => Promise<void>
  fetchOrders: (shopId: string, limit?: number) => Promise<void>
  subscribeToOrder: (orderId: string) => () => void
}

export const useOrderStore = create<OrderState>()(
  immer((set, get) => ({
    orders: [],
    currentOrder: null,
    isLoading: false,

    createOrder: async (
      shopId: string,
      cashierId: string,
      items: CartItem[],
      paymentMethod: 'cash' | 'qr' | 'card',
      discount: number = 0,
      taxRate: number = Config.tax.rate
    ): Promise<Order> => {
      set((state) => {
        state.isLoading = true
      })

      try {
        const subtotal = items.reduce((sum, item) => sum + item.subtotal, 0)
        const discountAmount = subtotal * (discount / 100)
        // Tax is inclusive (VAT already in price): extract tax from the after-discount amount
        const afterDiscount = subtotal - discountAmount
        const taxAmount = afterDiscount * (taxRate / (1 + taxRate))
        const totalAmount = afterDiscount

        // 1. Insert order (order_number auto-generated by DB sequence)
        const { data: order, error: orderError } = await supabase
          .from('orders')
          .insert({
            shop_id: shopId,
            cashier_id: cashierId,
            subtotal,
            discount_amount: discountAmount,
            tax_amount: taxAmount,
            total_amount: totalAmount,
            payment_method: paymentMethod,
            status: 'pending',
          })
          .select()
          .single()

        if (orderError) throw orderError

        // 2. Insert order items (no product_name column)
        const orderItems = items.map((item) => ({
          order_id: order.id,
          product_id: item.product.id,
          quantity: item.quantity,
          unit_price: item.product.price,
          subtotal: item.subtotal,
        }))

        const { error: itemsError } = await supabase
          .from('order_items')
          .insert(orderItems)

        if (itemsError) throw itemsError

        // 3. Insert pending payment
        const qrPayload =
          paymentMethod === 'qr'
            ? generatePromptPayPayload(Config.promptpay.id, totalAmount)
            : undefined

        const { data: payment, error: paymentError } = await supabase
          .from('payments')
          .insert({
            order_id: order.id,
            method: paymentMethod,
            amount: totalAmount,
            qr_payload: qrPayload,
            transaction_ref: generateQRReference(),
            status: 'pending',
          })
          .select()
          .single()

        if (paymentError) throw paymentError

        const fullOrder: OrderWithItems = {
          ...order,
          items: orderItems.map((item, i) => ({ ...item, id: `temp-${i}` })),
          payment,
        }

        set((state) => {
          state.currentOrder = fullOrder as any
          state.isLoading = false
        })

        return order as Order
      } catch (err: any) {
        set((state) => {
          state.isLoading = false
        })
        throw err
      }
    },

    updateOrderStatus: async (orderId: string, status: string) => {
      const updateData: any = { status }
      if (status === 'completed') {
        updateData.completed_at = new Date().toISOString()
      }
      const { error } = await supabase
        .from('orders')
        .update(updateData)
        .eq('id', orderId)

      if (error) throw error

      set((state) => {
        const order = state.orders.find((o) => o.id === orderId)
        if (order) order.status = status as any
        if (state.currentOrder?.id === orderId) {
          state.currentOrder.status = status as any
        }
      })
    },

    completeOrder: async (orderId: string, paymentData: Partial<Payment>, confirmationType: 'manual' | 'auto', confirmedBy?: string) => {
      const { error: paymentError } = await supabase
        .from('payments')
        .update({
          ...paymentData,
          status: 'success',
          confirmation_type: confirmationType,
          confirmed_by: confirmedBy ?? null,
          updated_at: new Date().toISOString(),
        })
        .eq('order_id', orderId)

      if (paymentError) throw paymentError

      const { error: orderError } = await supabase
        .from('orders')
        .update({
          status: 'completed',
          completed_at: new Date().toISOString(),
        })
        .eq('id', orderId)

      if (orderError) throw orderError

      set((state) => {
        const order = state.orders.find((o) => o.id === orderId)
        if (order) order.status = 'completed'
        if (state.currentOrder?.id === orderId) {
          state.currentOrder.status = 'completed'
        }
      })
    },

    fetchOrders: async (shopId: string, limit: number = 50) => {
      set((state) => {
        state.isLoading = true
      })

      try {
        const { data, error } = await supabase
          .from('orders')
          .select('*, items:order_items(*), payment:payments(*, confirmed_by_profile:profiles!payments_confirmed_by_fkey(full_name))')
          .eq('shop_id', shopId)
          .order('created_at', { ascending: false })
          .limit(limit)

        if (error) throw error

        set((state) => {
          state.orders = (data || []).map((o: any) => {
            const rawPayment = o.payment?.[0]
            const confirmedByProfile = rawPayment?.confirmed_by_profile ?? undefined
            const payment = rawPayment
              ? { ...rawPayment, confirmed_by_profile: undefined }
              : undefined
            return {
              ...o,
              payment,
              confirmedByProfile,
            }
          }) as OrderWithItems[]
          state.isLoading = false
        })
      } catch (err) {
        set((state) => {
          state.isLoading = false
        })
      }
    },

    subscribeToOrder: (orderId: string) => {
      const channel = supabase
        .channel(`order-payment:${orderId}`)
        .on(
          'postgres_changes',
          {
            event: 'UPDATE',
            schema: 'public',
            table: 'payments',
            filter: `order_id=eq.${orderId}`,
          },
          (payload) => {
            const newPayment = payload.new as Payment
            set((state) => {
              if (state.currentOrder?.id === orderId) {
                state.currentOrder.payment = newPayment
                if (newPayment.status === 'success') {
                  state.currentOrder.status = 'completed'
                }
              }
            })
            // Auto-confirm: when payment arrives as success from external source
            if (newPayment.status === 'success' && !newPayment.confirmation_type) {
              get().completeOrder(orderId, newPayment, 'auto')
            }
          }
        )
        .subscribe()

      return () => {
        channel.unsubscribe()
      }
    },
  }))
)
